DROP DATABASE IF EXISTS runners;
CREATE DATABASE runners;

-- Connect to running database
\c runners;

CREATE TABLE Runner(
    RunnerId INT,
    FirstName VARCHAR(200),
    LastName VARCHAR(200),
    HomeZip INT,
    PRIMARY KEY(RunnerId)
);

CREATE TABLE Zip(
    Zip1 INT,
    Zip2 INT,
    Distance INT
);

CREATE TABLE Race(
    RaceId INT,
    RaceZip INT,
    PRIMARY KEY(RaceId)
);

CREATE TABLE Results(
    RaceId INT,
    RunnerId INT,
    Time INT
);

